class CartItem {
  constructor(id, id_item, title, price, size, gender, category, quantity, total) {
    this.id = id;
    this.id = id_item;
    this.title = title;
    this.price = price;
    this.size = size;
    this.gender = gender;
    this.category = category;
    this.quantity = quantity;
    this.total = total;
  }
}
