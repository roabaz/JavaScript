class Sales {
  constructor(
    id,
    name,
    adress,
    phone,
    shipTime,
    cardNumber,
    expireDate,
    code,
    products
  ) {
    this.id = id;
    this.name = name;
    this.adress = adress;
    this.phone = phone;
    this.shipTime = shipTime;
    this.cardNumber = cardNumber;
    this.expireDate = expireDate;
    this.code = code;
    this.products = products;
  }
}
